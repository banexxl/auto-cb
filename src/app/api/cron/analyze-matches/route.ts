function filterEnabledSelections(selections: MatchSelection[]) {
  return selections.map(normalizeSelection).filter(isEnabledAnalysisSelection);
}

function mapToProposedBetBody(selection: MatchSelection): ProposedBetBody {
  return {
    currency: "USDT",
    eventId: selection.matchId.toString(),
    marketUrl: selection.marketUrl,
    outcome: selection.outcome,
    price: selection.price,
    referenceId: randomUUID(),
    stake: PAYIN,
  };
}

function calculateTotalQuota(selections: MatchSelection[]) {
  if (selections.length === 0) {
    return null;
  }

  return selections.reduce((totalQuota, selection) => totalQuota * selection.price, 1);
}

function isQuotaInRange(totalQuota: number | null) {
  return totalQuota !== null && totalQuota >= MIN_TOTAL_QUOTA && totalQuota <= MAX_TOTAL_QUOTA;
}

function getAnalysisByCandidateId(analysis: AiTicketAnalysis) {
  return new Map(analysis.analyzedSelections.map((selectionAnalysis) => [selectionAnalysis.candidateId, selectionAnalysis]));
}

function getHighProbabilitySelections(selections: MatchSelection[], analysis: AiTicketAnalysis) {
  const analysisByCandidateId = getAnalysisByCandidateId(analysis);

  return selections.filter((selection) => {
    const selectionAnalysis = analysisByCandidateId.get(selection.candidateId);
    return selectionAnalysis !== undefined && selectionAnalysis.assessedProbability > MIN_MODEL_PROBABILITY;
  });
}

function buildSelectedTicketSelections(selections: MatchSelection[], analysis: AiTicketAnalysis) {
  const highProbabilitySelections = getHighProbabilitySelections(selections, analysis);
  const highProbabilityIds = new Set(highProbabilitySelections.map((selection) => selection.candidateId));
  const selectedIds = analysis.selectedCandidateIds.filter((candidateId) => highProbabilityIds.has(candidateId));
  const selectedIdSet = new Set(selectedIds);

  return highProbabilitySelections.filter((selection) => selectedIdSet.has(selection.candidateId));
}

function groupSelectionsByMatch(selections: MatchSelection[]) {
  const groups = new Map<number, MatchSelection[]>();

  selections.forEach((selection) => {
    const group = groups.get(selection.matchId) ?? [];
    group.push(selection);
    groups.set(selection.matchId, group);
  });

  return Array.from(groups.values()).sort((left, right) => {
    const leftCutoff = left[0]?.cutoffTime ? new Date(left[0].cutoffTime).getTime() : Number.MAX_SAFE_INTEGER;
    const rightCutoff = right[0]?.cutoffTime ? new Date(right[0].cutoffTime).getTime() : Number.MAX_SAFE_INTEGER;

    return leftCutoff - rightCutoff;
  });
}

function getProjectedQuota(currentSelections: MatchSelection[], nextSelection: MatchSelection) {
  const currentQuota = calculateTotalQuota(currentSelections) ?? 1;
  return currentQuota * nextSelection.price;
}

function getIncrementalSelection(currentSelections: MatchSelection[], candidateSelections: MatchSelection[], analysis: AiTicketAnalysis) {
  const selectedCandidates = buildSelectedTicketSelections(candidateSelections, analysis);

  return selectedCandidates.find((selection) => getProjectedQuota(currentSelections, selection) <= MAX_TOTAL_QUOTA);
}

function mergeAnalyses(analyses: AiTicketAnalysis[], selectedSelections: MatchSelection[]): AiTicketAnalysis {
  const analyzedSelections = new Map<string, SelectionAnalysis>();

  analyses.forEach((analysis) => {
    analysis.analyzedSelections.forEach((selectionAnalysis) => {
      analyzedSelections.set(selectionAnalysis.candidateId, selectionAnalysis);
    });
  });

  return {
    decision: selectedSelections.length > 0 ? "PROPOSE_TICKET" : "NO_BET",
    message: selectedSelections.length > 0 ? "Incremental basketball ticket analysis completed." : NO_VALID_TICKET_MESSAGE,
    selectedCandidateIds: selectedSelections.map((selection) => selection.candidateId),
    analyzedSelections: Array.from(analyzedSelections.values()),
  };
}

async function buildIncrementalTicket(openaiApiKey: string, skillPrompt: string, enabledSelections: MatchSelection[]) {
  const selectedSelections: MatchSelection[] = [];
  const analyses: AiTicketAnalysis[] = [];
  const persistedMatches: StoredMatchSelection[] = [];
  let ticketId: SportTicketInsertResult["id"] | null = null;

  for (const matchSelections of groupSelectionsByMatch(enabledSelections)) {
    const currentQuota = calculateTotalQuota(selectedSelections);

    if (isQuotaInRange(currentQuota)) {
      break;
    }

    const analysis = await createAnalysis(openaiApiKey, skillPrompt, matchSelections, selectedSelections);
    analyses.push(analysis);

    let nextSelection: MatchSelection | undefined;
    let nextProposedBetBody: ProposedBetBody | undefined;

    if (analysis.decision === "PROPOSE_TICKET") {
      nextSelection = getIncrementalSelection(selectedSelections, matchSelections, analysis);

      if (nextSelection) {
        nextProposedBetBody = mapToProposedBetBody(nextSelection);
        selectedSelections.push(nextSelection);
      }
    }

    const matchProposedBetBodies = nextProposedBetBody ? [nextProposedBetBody] : [];
    persistedMatches.push(...buildStoredMatches(matchSelections, analysis, matchProposedBetBodies));

    if (ticketId === null) {
      ticketId = await insertSportTicket(buildSportTicketDefaults(persistedMatches));
    } else {
      await updateSportTicket(ticketId, buildSportTicketDefaultUpdate(persistedMatches));
    }

    if (isQuotaInRange(calculateTotalQuota(selectedSelections))) {
      break;
    }
  }

  return {
    analysis: mergeAnalyses(analyses, selectedSelections),
    persistedMatches,
    selectedSelections,
    ticketId,
  };
}

function buildStoredMatches(
  selections: MatchSelection[],
  analysis: AiTicketAnalysis,
  proposedBetBodies: ProposedBetBody[] = [],
): StoredMatchSelection[] {
  const analysisByCandidateId = getAnalysisByCandidateId(analysis);
  const proposedBetByCandidateId = new Map(
    proposedBetBodies.map((proposedBetBody) => [
      createCandidateId({
        matchId: Number(proposedBetBody.eventId),
        marketUrl: proposedBetBody.marketUrl,
        outcome: proposedBetBody.outcome,
      }),
      proposedBetBody,
    ]),
  );

  return selections.flatMap((selection) => {
    const selectionAnalysis = analysisByCandidateId.get(selection.candidateId);

    if (!selectionAnalysis) {
      return [];
    }

    return {
      ...selection,
      analysis: selectionAnalysis,
      proposedBetBody: proposedBetByCandidateId.get(selection.candidateId),
    };
  });
}

