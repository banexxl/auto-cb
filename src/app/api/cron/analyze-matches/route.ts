function buildPublicProposedBets(persistedMatches: StoredMatchSelection[]) {
  return persistedMatches
    .filter((storedMatch): storedMatch is StoredMatchSelection & { proposedBetBody: ProposedBetBody } =>
      storedMatch.proposedBetBody !== undefined,
    )
    .map((storedMatch) => toPublicProposedBet(storedMatch, storedMatch.proposedBetBody));
}
function buildSportTicketDefaults(matches: StoredMatchSelection[], message: string | null = null): SportTicketInsert {
  return {
    status: "PENDING_REVIEW",
    currency: "USDT",
    matches,
    total_quota: null,
    payin: 0,
    possible_payout: null,
    is_played: false,
    played_successfully: null,
    message,
    external_ticket_id: null,
    reference_id: randomUUID(),
  };
}

function buildSportTicketInsert(params: {
  status: SportTicketStatus;
  matches: StoredMatchSelection[];
  totalQuota: number | null;
  payin: number;
  message: string;
  playedSuccessfully: boolean | null;
}): SportTicketInsert {
  return {
    ...buildSportTicketDefaults(params.matches),
    status: params.status,
    total_quota: params.totalQuota,
    payin: params.payin,
    possible_payout: params.totalQuota === null ? null : params.totalQuota * params.payin,
    played_successfully: params.playedSuccessfully,
    message: params.message,
  };
}

function buildSportTicketDefaultUpdate(matches: StoredMatchSelection[]): SportTicketUpdate {
  const { reference_id: _referenceId, ...defaults } = buildSportTicketDefaults(matches);
  return defaults;
}

async function insertSportTicket(ticket: SportTicketInsert) {
  console.info("[cron:analyze-matches:sport_tickets:insert:start]", {
    status: ticket.status,
    matchesCount: ticket.matches.length,
    totalQuota: ticket.total_quota,
    payin: ticket.payin,
    possiblePayout: ticket.possible_payout,
  });

  const supabase = await createClient();
  const { data, error } = await supabase
    .from("sport_tickets")
    .insert(ticket)
    .select("id")
    .single<SportTicketInsertResult>();

  if (error) {
    console.error("[cron:analyze-matches:sport_tickets:insert:error]", error);
    throw new Error("Unable to persist generated sport ticket.");
  }

  if (!data) {
    console.error("[cron:analyze-matches:sport_tickets:insert:empty]");
    throw new Error("Unable to persist generated sport ticket.");
  }

  console.info("[cron:analyze-matches:sport_tickets:insert:success]", {
    ticketId: data.id,
    status: ticket.status,
    matchesCount: ticket.matches.length,
  });

  return data.id;
}

async function updateSportTicket(ticketId: SportTicketInsertResult["id"], ticket: SportTicketUpdate) {
  console.info("[cron:analyze-matches:sport_tickets:update:start]", {
    ticketId,
    status: ticket.status,
    matchesCount: ticket.matches.length,
    totalQuota: ticket.total_quota,
    payin: ticket.payin,
    possiblePayout: ticket.possible_payout,
  });

  const supabase = await createClient();
  const { data, error } = await supabase
    .from("sport_tickets")
    .update(ticket)
    .eq("id", ticketId)
    .select("id")
    .single<SportTicketInsertResult>();

  if (error) {
    console.error("[cron:analyze-matches:sport_tickets:update:error]", { ticketId, error });
    throw new Error("Unable to update generated sport ticket.");
  }

  if (!data) {
    console.error("[cron:analyze-matches:sport_tickets:update:empty]", { ticketId });
    throw new Error("Unable to update generated sport ticket.");
  }

  console.info("[cron:analyze-matches:sport_tickets:update:success]", {
    ticketId: data.id,
    status: ticket.status,
    matchesCount: ticket.matches.length,
  });

  return data.id;
}

async function loadSportAnalystSkills() {
  const skillDirectory = path.join(process.cwd(), SPORT_ANALYST_SKILL_DIR);
  const entries = await readdir(skillDirectory, { withFileTypes: true });
  const markdownFiles = entries
    .filter((entry) => entry.isFile() && entry.name.toLowerCase().endsWith(".md"))
    .map((entry) => entry.name)
    .sort((left, right) => left.localeCompare(right));

  const contents = await Promise.all(
    markdownFiles.map(async (fileName) => {
      const filePath = path.join(skillDirectory, fileName);
      const content = await readFile(filePath, "utf8");

      return `# ${fileName}\n\n${content.trim()}`;
    }),
  );

  return contents.join("\n\n---\n\n");
}

function buildAnalysisInput(selections: MatchSelection[], currentTicketSelections: MatchSelection[] = []) {
  return [
    "Analyze only this one basketball match group and decide whether to add at most one selection from it to the current ticket.",
    "Return JSON only. Do not include markdown or prose outside the JSON object.",
    `Only select an outcome where your assessed probability is greater than ${MIN_MODEL_PROBABILITY}%.`,
    `The final combined decimal quota must be between ${MIN_TOTAL_QUOTA} and ${MAX_TOTAL_QUOTA}.`,
    "If adding a selection from this match would be weak, unsafe, or push quota above the maximum, return decision NO_BET.",
    "Do not place any bet or call any betting API. This route only creates a stored proposal.",
    "Consider form, H2H, injuries, suspensions, expected/confirmed lineups, motivation, schedule congestion, relevant news, market integrity concerns, and any reasons to avoid a bet.",
    "Current ticket selections already accepted by previous analysis steps:",
    JSON.stringify(currentTicketSelections, null, 2),
    "Candidate selections for this one basketball match:",
    JSON.stringify(selections, null, 2),
  ].join("\n\n");
}

function getAnalysisJsonSchema() {
  return {
    type: "object",
    additionalProperties: false,
    properties: {
      decision: { type: "string", enum: ["PROPOSE_TICKET", "NO_BET"] },
      message: { type: "string" },
      selectedCandidateIds: {
        type: "array",
        items: { type: "string" },
      },
      analyzedSelections: {
        type: "array",
        items: {
          type: "object",
          additionalProperties: false,
          properties: {
            candidateId: { type: "string" },
            assessedProbability: { type: "number", minimum: 0, maximum: 100 },
            confidence: { type: "string", enum: ["HIGH", "MEDIUM", "LOW"] },
            summary: { type: "string" },
            risks: {
              type: "array",
              items: { type: "string" },
            },
          },
          required: ["candidateId", "assessedProbability", "confidence", "summary", "risks"],
        },
      },
    },
    required: ["decision", "message", "selectedCandidateIds", "analyzedSelections"],
  };
}

function extractOutputText(response: OpenAIResponsesResponse) {
  if (typeof response.output_text === "string" && response.output_text.trim()) {
    return response.output_text.trim();
  }

  const outputText = response.output
    ?.flatMap((output) => output.content ?? [])
    .filter((content) => content.type === "output_text" && typeof content.text === "string")
    .map((content) => content.text)
    .join("\n")
    .trim();

  if (!outputText) {
    throw new Error("No analysis text returned by OpenAI.");
  }

  return outputText;
}

function isSelectionAnalysis(value: unknown): value is SelectionAnalysis {
  if (!value || typeof value !== "object") {
    return false;
  }

  const analysis = value as Partial<SelectionAnalysis>;

  return (
    typeof analysis.candidateId === "string" &&
    typeof analysis.assessedProbability === "number" &&
    Number.isFinite(analysis.assessedProbability) &&
    (analysis.confidence === "HIGH" || analysis.confidence === "MEDIUM" || analysis.confidence === "LOW") &&
    typeof analysis.summary === "string" &&
    Array.isArray(analysis.risks) &&
    analysis.risks.every((risk) => typeof risk === "string")
  );
}

function parseAiTicketAnalysis(text: string): AiTicketAnalysis {
  const parsed = JSON.parse(text) as unknown;

  if (!parsed || typeof parsed !== "object") {
    throw new Error("OpenAI analysis did not return a JSON object.");
  }

  const analysis = parsed as Partial<AiTicketAnalysis>;

  if (
    (analysis.decision !== "PROPOSE_TICKET" && analysis.decision !== "NO_BET") ||
    typeof analysis.message !== "string" ||
    !Array.isArray(analysis.selectedCandidateIds) ||
    !analysis.selectedCandidateIds.every((candidateId) => typeof candidateId === "string") ||
    !Array.isArray(analysis.analyzedSelections) ||
    !analysis.analyzedSelections.every(isSelectionAnalysis)
  ) {
    throw new Error("OpenAI analysis JSON does not match the expected ticket schema.");
  }

  return {
    decision: analysis.decision,
    message: analysis.message,
    selectedCandidateIds: analysis.selectedCandidateIds,
    analyzedSelections: analysis.analyzedSelections,
  };
}

async function createAnalysis(
  openaiApiKey: string,
  skillPrompt: string,
  selections: MatchSelection[],
  currentTicketSelections: MatchSelection[] = [],
) {
  const payload: OpenAIResponsesPayload = {
    model: OPENAI_MODEL,
    instructions: skillPrompt,
    input: buildAnalysisInput(selections, currentTicketSelections),
    store: false,
    text: {
      format: {
        type: "json_schema",
        name: "sport_ticket_analysis",
        strict: true,
        schema: getAnalysisJsonSchema(),
      },
    },
  };

  const response = await fetch(OPENAI_RESPONSES_URL, {
    method: "POST",
    headers: {
      authorization: `Bearer ${openaiApiKey}`,
      "content-type": "application/json",
    },
    body: JSON.stringify(payload),
    signal: AbortSignal.timeout(OPENAI_RESPONSES_TIMEOUT_MS),
  });

  if (!response.ok) {
    let message = `OpenAI Responses API request failed with status ${response.status}.`;

    try {
      const errorBody = (await response.json()) as { error?: { message?: string } };
      message = errorBody.error?.message ?? message;
    } catch {
      // Keep the generic upstream error message.
    }

    throw new Error(message);
  }

  return parseAiTicketAnalysis(extractOutputText((await response.json()) as OpenAIResponsesResponse));
}

async function insertFailedTicket(message: string, matches: StoredMatchSelection[] = []) {
  const ticket = buildSportTicketInsert({
    status: "FAILED",
    matches,
    totalQuota: null,
    payin: 0,
    message,
    playedSuccessfully: false,
  });
  const ticketId = await insertSportTicket(ticket);

  return { ticket, ticketId };
}

async function insertDefaultTicket(matches: StoredMatchSelection[] = [], message: string | null = null) {
  const ticket = buildSportTicketDefaults(matches, message);
  const ticketId = await insertSportTicket(ticket);

  return { ticket, ticketId };
}

export async function POST(request: Request) {
  const missingCronEnvVars = getMissingCronEnvVars();

  if (missingCronEnvVars.length > 0) {
    return jsonError("Cron analysis endpoint is not configured.", 500);
  }

  const cronSecret = process.env.CRON_SECRET as string;

  if (request.headers.get("x-cron-secret") !== cronSecret) {
    return jsonError("Unauthorized cron request.", 401);
  }

  let body: unknown;

  try {
    body = await request.json();
  } catch {
    return jsonError("Invalid JSON request body.", 400);
  }

  try {
    const enabledBasketballSelections = await fetchEnabledBasketballAnalysisSelections();

    if (enabledBasketballSelections.length === 0) {
      const { ticket, ticketId } = await insertDefaultTicket([], NO_MARKETS_MESSAGE);

      return jsonNoEnabledMatches(ticketId, ticket);
    }

    const matches = getMatchesFromBody(body);
    let candidateSelections: MatchSelection[];

    if (matches === undefined) {
      candidateSelections = enabledBasketballSelections;
    } else if (Array.isArray(matches) && matches.every(isMatchSelection)) {
      candidateSelections = matches.map(normalizeSelection);
    } else {
      return jsonError("Request body must include a matches array of MatchSelection objects or omit matches to fetch basketball selections.", 400);
    }

    if (candidateSelections.length === 0) {
      const { ticket, ticketId } = await insertDefaultTicket([], NO_MARKETS_MESSAGE);

      return jsonNoEnabledMatches(ticketId, ticket);
    }

    const enabledSelections = filterEnabledSelections(candidateSelections);

    if (enabledSelections.length === 0) {
      const { ticket, ticketId } = await insertDefaultTicket([], NO_MARKETS_MESSAGE);

      return jsonNoEnabledMatches(ticketId, ticket);
    }

    const matchGroups = groupSelectionsByMatch(enabledSelections);
    const randomMatchSelections = matchGroups.length > 0
      ? matchGroups[Math.floor(Math.random() * matchGroups.length)]
        .filter((s) => s.price >= 1.2 && s.price <= 2.2)
        .slice(0, 20)
      : [];

    if (randomMatchSelections.length === 0) {
      const { ticket, ticketId } = await insertDefaultTicket([], NO_MARKETS_MESSAGE);

      return jsonNoEnabledMatches(ticketId, ticket);
    }

    const missingAnalysisEnvVars = getMissingAnalysisEnvVars();

    if (missingAnalysisEnvVars.length > 0) {
      return jsonError("Cron analysis endpoint is missing OpenAI configuration.", 500);
    }

    const openaiApiKey = process.env.OPENAI_API_KEY as string;
    const skillPrompt = await loadSportAnalystSkills();
    const { persistedMatches, selectedSelections, ticketId } = await buildIncrementalTicket(openaiApiKey, skillPrompt, randomMatchSelections);
    const totalQuota = calculateTotalQuota(selectedSelections);

    if (ticketId === null) {
      const { ticketId: failedTicketId } = await insertFailedTicket(NO_VALID_TICKET_MESSAGE);

      return NextResponse.json({
        success: true,
        ticketId: failedTicketId,
        status: "FAILED",
        message: NO_VALID_TICKET_MESSAGE,
        proposedBets: [],
      });
    }

    if (selectedSelections.length === 0 || !isQuotaInRange(totalQuota)) {
      const failedTicket = buildSportTicketInsert({
        status: "FAILED",
        matches: persistedMatches,
        totalQuota: null,
        payin: 0,
        message: NO_VALID_TICKET_MESSAGE,
        playedSuccessfully: false,
      });
      const { reference_id: _referenceId, ...failedTicketUpdate } = failedTicket;
      await updateSportTicket(ticketId, failedTicketUpdate);

      return NextResponse.json({
        success: true,
        ticketId,
        status: failedTicket.status,
        message: failedTicket.message,
        proposedBets: [],
      });
    }

    const pendingTicketInsert = buildSportTicketInsert({
      status: "PENDING_REVIEW",
      matches: persistedMatches,
      totalQuota,
      payin: PAYIN,
      message: PENDING_TICKET_MESSAGE,
      playedSuccessfully: null,
    });
    const { reference_id: _referenceId, ...pendingTicketUpdate } = pendingTicketInsert;
    await updateSportTicket(ticketId, pendingTicketUpdate);

    return NextResponse.json({
      success: true,
      ticketId,
      status: pendingTicketInsert.status,
      message: pendingTicketInsert.message,
      proposedBets: buildPublicProposedBets(persistedMatches),
    });
  } catch (error) {
    const isTimeout =
      error instanceof DOMException && error.name === "TimeoutError";

    const message = isTimeout
      ? "OpenAI analysis timed out. Try reducing candidate selections or increasing CRON_OPENAI_RESPONSES_TIMEOUT_MS."
      : error instanceof Error
        ? error.message
        : "Unable to analyze matches.";

    console.error("[cron:analyze-matches:error]", error);
    return jsonError(message, 500);
  }
}
